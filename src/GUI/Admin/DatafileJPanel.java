    public void loadDatafile() {
        String[] header = {"File name","File id"};
        listDatafile = DatafileDAO.getInstance().getAllDatafile();
        DefaultTableModel modelTableDb = new DefaultTableModel(header, 0) {
            @Override
            public boolean isCellEditable(int rowIndex, int colIndex) {
                return false;
            }
        };

        for (Datafile dt : listDatafile) {
            Object[] row = {dt.getFile_name(), dt.getFile_id()};
            modelTableDb.addRow(row);
        }
        
        TableRowSorter<TableModel> rowSorter = null;
        jlbSoLuongDatafile.setText("Số lượng datafile: ");
        String txtSoLuongDatafile = jlbSoLuongDatafile.getText() + listDatafile.size();
        jlbSoLuongDatafile.setText(txtSoLuongDatafile);
        
        jtDatafile.setModel(modelTableDb);
        jtDatafile.setRowSorter(rowSorter);

        jtDatafile.getTableHeader().setFont(new Font("Arial", Font.BOLD, 14));
        jtDatafile.setFont(new Font("Arial", Font.PLAIN, 14));
        jtDatafile.getTableHeader().setPreferredSize(new Dimension(100, 50));
        jtDatafile.setRowHeight(50);
        jtDatafile.validate();
        jtDatafile.repaint();

        jspDatafile.setPreferredSize(new Dimension(1350, 400));

        jpnView.removeAll();
        jpnView.setLayout(new CardLayout());
        jpnView.add(jspDatafile);
        jpnView.validate();
        jpnView.repaint();
    }
