    private void jtDatafileMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtDatafileMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2 && jtDatafile.getSelectedRow() != -1) {
            int index = jtDatafile.getSelectedRow();

            Datafile dtf = listDatafile.get(index);

            int file_id = dtf.getFile_id();

            DatafileJFrame frame = new DatafileJFrame(file_id);
            frame.setResizable(false);

            JDialog dialog = new JDialog();

            dialog.setModal(true);
            dialog.getContentPane().add(frame.getContentPane());
            dialog.pack();
            dialog.setLocationRelativeTo(null);
            dialog.setTitle("Thông tin chi tiết Datafile");
            dialog.setVisible(true);
        }
    }//GEN-LAST:event_jtDatafileMouseClicked

